    @staticmethod
    def _format_growth_simple(title: str, data: dict) -> str:
        """Handles the Growth & Discovery Agent's simple fields, which come back
        as either {"content": ...} or {"heading"/"title": ..., "body"/"content": ...}."""
        if not data:
            return ""
        heading = data.get("heading") or data.get("title")
        body = data.get("body") or data.get("content", "")
        if heading:
            return f"# {heading}\n\n{body}"
        return f"# {title}\n\n{body}"

